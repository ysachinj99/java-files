// sample Datatype Program
public class datatypeprg
{
public static void main(String args [])
{
int  a=10;
char c= 'A';
float pi= 3.14f;
double dl=45.345;
String nm= "java";
System.out.println(" Welcome to Our Datatype program !!! ");
System.out.println("int       is :"+a);
System.out.println("char    is :"+c);
System.out.println("float   is :"+pi);
System.out.println("double is :"+dl);
System.out.println("String is :"+nm);
}
}